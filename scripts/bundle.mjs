#!/usr/bin/env node
/**
 * Bundle script: read annotate.js + annotate.css, emit annotate.bundle.js
 * with CSS injected at runtime via a <style> tag.
 *
 * Output is a drop-in single-script-tag distribution: consumers do not need
 * to link annotate.css separately when using the bundle.
 *
 * Idempotent: rerun anytime after `npm run build`.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const js = readFileSync(join(root, 'annotate.js'), 'utf8');
const css = readFileSync(join(root, 'annotate.css'), 'utf8');

// Escape backticks, ${ interpolation, and backslashes for safe template literal embedding.
function escapeForTemplate(s) {
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

const cssLiteral = '`' + escapeForTemplate(css) + '`';

const header = `/*!
 * annotate-js — bundled (CSS inlined)
 * Drop a single <script> tag and the stylesheet is injected automatically.
 * Generated by scripts/bundle.mjs — do not edit by hand.
 *
 * @license MIT
 * @see https://github.com/Buzz-Interactive/annotate
 */
(function () {
  if (typeof document === 'undefined') return;
  if (document.getElementById('annotate-js-inline-styles')) return;
  var s = document.createElement('style');
  s.id = 'annotate-js-inline-styles';
  s.textContent = ${cssLiteral};
  (document.head || document.documentElement).appendChild(s);
})();
`;

const out = header + '\n' + js;
writeFileSync(join(root, 'annotate.bundle.js'), out, 'utf8');

const jsKb = (Buffer.byteLength(js, 'utf8') / 1024).toFixed(1);
const cssKb = (Buffer.byteLength(css, 'utf8') / 1024).toFixed(1);
const bundleKb = (Buffer.byteLength(out, 'utf8') / 1024).toFixed(1);
console.log(`bundle: annotate.bundle.js  (${bundleKb} KB)`);
console.log(`  js:  ${jsKb} KB`);
console.log(`  css: ${cssKb} KB`);
